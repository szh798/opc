async function main() {
  const current = await prisma.reportSnapshot.findUnique({
    where: {
      userId_kind: {
        userId,
        kind: "ASSET_INVENTORY"
      }
    }
  });
  const currentData = current && current.data && typeof current.data === "object" ? current.data : {};
  const flowState = {
    ...(currentData.flowState && typeof currentData.flowState === "object" ? currentData.flowState : {}),
    conversationId: sessionId,
    inventoryStage: "report_generated",
    reviewStage: "",
    profileSnapshot,
    dimensionReports,
    nextQuestion: "",
    changeSummary: "",
    reportBrief,
    finalReport,
    reportVersion: "1",
    lastReportGeneratedAt: now,
    reportStatus: "ready",
    reportError: "",
    assetWorkflowKey: "firstInventory",
    isReview: "false",
    syncedAt: now
  };
  const data = {
    ...currentData,
    flowState,
    flowSections: {
      profileSnapshot: {
        "能力资产": ["能拆系统", "能落地工程"],
        "资源资产": ["OPC 项目", "真实开发环境"],
        "认知资产": ["资产复用", "自动化交付"],
        "关系资产": ["种子用户", "同频开发者"]
      },
      dimensionReports: {
        "能力资产小报告": ["产品拆解", "工程落地"],
        "资源资产小报告": ["OPC 代码库", "测试环境"],
        "认知资产小报告": ["一人公司方法论", "AI 产品判断"],
        "关系资产小报告": ["熟人试用", "案例共创"]
      },
      finalReport: {
        "能力资产小报告": ["系统拆解和工程实现"],
        "资源资产小报告": ["项目和文档可复用"],
        "认知资产小报告": ["资产化与自动化"],
        "关系资产小报告": ["种子用户切入"],
        "总资产报告": ["先卖小闭环服务"]
      }
    }
  };

  await prisma.reportSnapshot.upsert({
    where: {
      userId_kind: {
        userId,
        kind: "ASSET_INVENTORY"
      }
    },
    create: {
      userId,
      kind: "ASSET_INVENTORY",
      data
    },
    update: {
      data
    }
  });
}

